#!/usr/bin/env bash
# -*- coding: utf-8 -*-

# Скрипт для установки прав доступа на все файлы и директории
# в текущей директории для доступа пользователя _apt
# Версия: V3.4.176
# Дата: 2025.12.11
# Компания: ООО "НПА Вира-Реалтайм"

echo "=========================================="
echo "Установка прав доступа на файлы и директории"
echo "=========================================="
echo ""

# Получаем текущую директорию
CURRENT_DIR=$(pwd)
echo "Текущая директория: $CURRENT_DIR"
echo ""

# Подсчитываем количество директорий и файлов
DIR_COUNT=$(find . -type d | wc -l)
FILE_COUNT=$(find . -type f | wc -l)

echo "Найдено директорий: $DIR_COUNT"
echo "Найдено файлов: $FILE_COUNT"
echo ""

# Устанавливаем права на все директории: 755 (rwxr-xr-x)
echo "Установка прав 755 на все директории..."
find . -type d -exec chmod 755 {} \;
if [ $? -eq 0 ]; then
    echo "✓ Права на директории установлены успешно"
else
    echo "✗ Ошибка при установке прав на директории"
    exit 1
fi
echo ""

# Устанавливаем права на все файлы: 644 (rw-r--r--)
echo "Установка прав 644 на все файлы..."
find . -type f -exec chmod 644 {} \;
if [ $? -eq 0 ]; then
    echo "✓ Права на файлы установлены успешно"
else
    echo "✗ Ошибка при установке прав на файлы"
    exit 1
fi
echo ""

# Устанавливаем права 755 на исполняемые файлы (.sh, и другие скрипты)
echo "Установка прав 755 на исполняемые файлы..."
find . -type f \( -name "*.sh" -o -name "*.py" -o -name "winetricks" \) -exec chmod 755 {} \;
if [ $? -eq 0 ]; then
    echo "✓ Права на исполняемые файлы установлены успешно"
else
    echo "✗ Ошибка при установке прав на исполняемые файлы"
    exit 1
fi
echo ""

# Проверяем права на .deb файлы (если есть)
DEB_FILES=$(find . -name "*.deb" 2>/dev/null)
if [ -n "$DEB_FILES" ]; then
    echo "Найдены .deb файлы:"
    echo "$DEB_FILES" | while read -r file; do
        PERMS=$(stat -c "%a" "$file" 2>/dev/null || stat -f "%OLp" "$file" 2>/dev/null)
        echo "  $file - права: $PERMS"
    done
    echo ""
fi

echo "=========================================="
echo "Готово! Права доступа установлены."
echo "Пользователь _apt теперь может читать файлы."
echo "Исполняемые файлы имеют права на выполнение."
echo "=========================================="