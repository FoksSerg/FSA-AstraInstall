            print("   ‚úÖ –û–±–Ω–∞—Ä—É–∂–µ–Ω –∑–∞–ø—Ä–æ—Å: %s" % prompt_type)
            if response == '':
                print("   ‚úÖ –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –æ—Ç–≤–µ—Ç: Enter (–ø—É—Å—Ç–æ–π –æ—Ç–≤–µ—Ç)")
            else:
                print("   ‚úÖ –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –æ—Ç–≤–µ—Ç: %s" % response)
